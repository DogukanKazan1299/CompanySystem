    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        mesaj_yazisi.setText("");
        int selectedrow = yonetici_tablosu.getSelectedRow();
        
        if(selectedrow == -1) {
            if(model.getRowCount() == 0) {
                mesaj_yazisi.setText("Yönetici Tablosu Şu Anda Boş");
            }
            else{
                mesaj_yazisi.setText("Lütfen silinecek bir yönetici seçiniz..");
            }
        }
        else{
            int id = (int)model.getValueAt(selectedrow, 0);
            islemler.yoneticiSil(id);
            yoneticiGoruntule();
            mesaj_yazisi.setText(" Bir yönetici işten çıkarıldı...");
        }
        PatronBilgilendirmeEkrani patronBilgilendirmeEkrani = new PatronBilgilendirmeEkrani();
        patronBilgilendirmeEkrani.setLocation(WIDTH, WIDTH);
        patronBilgilendirmeEkrani.setTitle("Patron Bilgilendirme Ekranı");
        patronBilgilendirmeEkrani.setVisible(true);

        pBilgi.add(patronBilgilendirmeEkrani);
        String message = "Şirkette bir yönetici işten çıkarıldı...";

        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Message is Empty");
            return;
        }
        for (PersonelEkrani.PatronBilgi patronBilgi : pBilgi) {
            patronBilgi.notifyChanges(message);
        }
    }//GEN-LAST:event_silActionPerformed

    public void yoneticiGoruntule(){
        
        model.setRowCount(0);
        
        ArrayList<Yonetici> yoneticiler = new ArrayList<Yonetici>();
        
        yoneticiler = islemler.yoneticileriGetir();
        if(yoneticiler != null){
            for(Yonetici yonetici : yoneticiler){
                Object[] eklenecek = {yonetici.getId(),yonetici.getAd_soyad(),yonetici.getDepartman(),yonetici.getGiristarihi(),
                yonetici.getSozlesmesuresi(),yonetici.getSirketid(),yonetici.getY_maas()};
                
                model.addRow(eklenecek);
                
            }
        }
    }
