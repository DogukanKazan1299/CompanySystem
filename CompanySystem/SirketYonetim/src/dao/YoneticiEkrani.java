    private void y_ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_y_ekleActionPerformed
        mesaj_yazisi.setText("");
        
        int id = (Integer.parseInt(id_alani.getText()));
        String ad_soyad = ad_soyad_alani.getText();
        String departman = dept_alani.getText();
        Date giristarihi = Date.valueOf(tarih_alani.getText());
        float sozlesmesuresi = (Float.parseFloat(sozlesme_alani.getText()));
        int sirketid = (Integer.parseInt(sirket_id_alani.getText()));
        int y_maas = (Integer.parseInt(y_maas_alani.getText()));
        
        islemler.yoneticiEkle(id,ad_soyad,departman,giristarihi,sozlesmesuresi,sirketid,y_maas);
        
        yoneticiGoruntule();
        
        mesaj_yazisi.setText("Yeni yönetici eklendi...");
        
        PatronBilgilendirmeEkrani patronBilgilendirmeEkrani = new PatronBilgilendirmeEkrani();
            patronBilgilendirmeEkrani.setLocation(WIDTH, WIDTH);
            patronBilgilendirmeEkrani.setTitle("Patron Bilgilendirme Ekranı");
            patronBilgilendirmeEkrani.setVisible(true);
            
            pBilgi.add(patronBilgilendirmeEkrani);
        String message = "Şirkete yeni bir yönetici eklendi...";
       
       
        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Message is Empty");
            return;
        }
        for (PersonelEkrani.PatronBilgi patronBilgi : pBilgi) {
            patronBilgi.notifyChanges(message);
        }
    }//GEN-LAST:event_y_ekleActionPerformed

    private void yoneticiGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yoneticiGuncelleActionPerformed
         //int id = (Integer.parseInt(id_alani.getText()));
        String ad_soyad = ad_soyad_alani.getText();
        String departman = dept_alani.getText();
        Date giristarihi = Date.valueOf(tarih_alani.getText());
        float sozlesmesuresi = (Float.parseFloat(sozlesme_alani.getText()));
        int sirketid = (Integer.parseInt(sirket_id_alani.getText()));
        int y_maas = (Integer.parseInt(y_maas_alani.getText()));
        
        int selectedrow = yonetici_tablosu.getSelectedRow();
        
        if(selectedrow == -1){
            if(model.getRowCount() ==0){
                mesaj_yazisi.setText("Personel Tablosu şu anda boş..");
            }else{
                mesaj_yazisi.setText("Güncellenecek bir yönetici seçin");
            }
        }
        else{
            
            
            
            int id= (int)model.getValueAt(selectedrow, 0);
            
            islemler.yoneticiGuncelle(id,ad_soyad,departman,giristarihi,sozlesmesuresi,sirketid,y_maas);
            yoneticiGoruntule();
            mesaj_yazisi.setText("Yönetici bilgileri başarıyla güncellendi...");
             
        }
        
        PatronBilgilendirmeEkrani patronBilgilendirmeEkrani = new PatronBilgilendirmeEkrani();
        patronBilgilendirmeEkrani.setLocation(WIDTH, WIDTH);
        patronBilgilendirmeEkrani.setTitle("Patron Bilgilendirme Ekranı");
        patronBilgilendirmeEkrani.setVisible(true);

        pBilgi.add(patronBilgilendirmeEkrani);
        String message = "Şirketre  bir yöneticinin bilgileri güncellendi...";

        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Message is Empty");
            return;
        }
        for (PersonelEkrani.PatronBilgi patronBilgi : pBilgi) {
            patronBilgi.notifyChanges(message);
        }
    }//GEN-LAST:event_yoneticiGuncelleActionPerformed

