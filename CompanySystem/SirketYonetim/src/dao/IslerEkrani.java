    private void ısGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ısGuncelleActionPerformed
        //int id = (Integer.parseInt(id_alani.getText()));
        String isadi = is_adi_alani.getText();
        String isalani = is_alani.getText();
        String isyapilanfirma = firma_alani.getText();
        float sozlesmesuresi = (Float.parseFloat(sozlesme_alani.getText()));
        int isgeliri = (Integer.parseInt(gelir_alani.getText()));
        int digergelirler = (Integer.parseInt(digerGelirler_alani.getText()));
        int sirketid = (Integer.parseInt(sirket_id_alani.getText()));

        int selectedrow = isler_tablosu.getSelectedRow();

        if (selectedrow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("İşler Tablosu şu anda boş..");
            } else {
                mesaj_yazisi.setText("Güncellenecek bir iş seçin");
            }
            PatronBilgilendirmeEkrani patronBilgilendirmeEkrani = new PatronBilgilendirmeEkrani();
            patronBilgilendirmeEkrani.setLocation(WIDTH, WIDTH);
            patronBilgilendirmeEkrani.setTitle("Patron Bilgilendirme Ekranı");
            patronBilgilendirmeEkrani.setVisible(true);

            pBilgi.add(patronBilgilendirmeEkrani);
            String message = "Şirkette yapılan  işlerden birtanesi güncellendi...";

            if (message.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Message is Empty");
                return;
            }
            for (PersonelEkrani.PatronBilgi patronBilgi : pBilgi) {
                patronBilgi.notifyChanges(message);
            }

        } else {

            int id = (int) model.getValueAt(selectedrow, 0);

            islemler.isGuncelle(id, isadi, isalani, isyapilanfirma, sozlesmesuresi, isgeliri, digergelirler, sirketid);
            isleriGoruntule();
            mesaj_yazisi.setText("İşler başarıla güncellendi...");

        }
    }//GEN-LAST:event_ısGuncelleActionPerformed

    private void silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_silActionPerformed
        mesaj_yazisi.setText("");
        int selectedrow = isler_tablosu.getSelectedRow();

        if (selectedrow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("İş Tablosu Şu Anda Boş");
            } else {
                mesaj_yazisi.setText("Lütfen silinecek bir iş seçiniz..");
            }
        } else {
            int id = (int) model.getValueAt(selectedrow, 0);
            islemler.isSil(id);
            isleriGoruntule();
            mesaj_yazisi.setText("İş başarıyla silindi");
            
            PatronBilgilendirmeEkrani patronBilgilendirmeEkrani = new PatronBilgilendirmeEkrani();
            patronBilgilendirmeEkrani.setLocation(WIDTH, WIDTH);
            patronBilgilendirmeEkrani.setTitle("Patron Bilgilendirme Ekranı");
            patronBilgilendirmeEkrani.setVisible(true);

            pBilgi.add(patronBilgilendirmeEkrani);
            String message = "Şirket işi tamamladı ve yapılan iş kaldırıldı...";

            if (message.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Message is Empty");
                return;
            }
            for (PersonelEkrani.PatronBilgi patronBilgi : pBilgi) {
                patronBilgi.notifyChanges(message);
            }

        }
    }//GEN-LAST:event_silActionPerformed

    public void isleriGoruntule() {

        model.setRowCount(0);

        ArrayList<Isler> isler = new ArrayList<Isler>();

        isler = islemler.isleriGetir();
        if (isler != null) {
            for (Isler is : isler) {
                Object[] eklenecek = {is.getId(), is.getIsadi(), is.getIsalani(), is.getIsyapilanfirma(),
                    is.getSozlesmesuresi(), is.getIsgeliri(), is.getDigergelirler(), is.getSirketid()};

                model.addRow(eklenecek);

            }
        }
    }

