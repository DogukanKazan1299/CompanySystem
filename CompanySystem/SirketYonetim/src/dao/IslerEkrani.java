    private void isEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_isEkleActionPerformed
        mesaj_yazisi.setText("");

        int id = (Integer.parseInt(id_alani.getText()));
        String isadi = is_adi_alani.getText();
        String isalani = is_alani.getText();
        String isyapilanfirma = firma_alani.getText();
        float sozlesmesuresi = (Float.parseFloat(sozlesme_alani.getText()));
        int isgeliri = (Integer.parseInt(gelir_alani.getText()));
        int digergelirler = (Integer.parseInt(digerGelirler_alani.getText()));
        int sirketid = (Integer.parseInt(sirket_id_alani.getText()));

        islemler.isEkle(id, isadi, isalani, isyapilanfirma, sozlesmesuresi, isgeliri, digergelirler, sirketid);

        isleriGoruntule();

        mesaj_yazisi.setText("Yeni iş eklendi...");

        PatronBilgilendirmeEkrani patronBilgilendirmeEkrani = new PatronBilgilendirmeEkrani();
        patronBilgilendirmeEkrani.setLocation(WIDTH, WIDTH);
        patronBilgilendirmeEkrani.setTitle("Patron Bilgilendirme Ekranı");
        patronBilgilendirmeEkrani.setVisible(true);

        pBilgi.add(patronBilgilendirmeEkrani);
        String message = "Şirket yeni bir iş aldı...";

        if (message.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Message is Empty");
            return;
        }
        for (PersonelEkrani.PatronBilgi patronBilgi : pBilgi) {
            patronBilgi.notifyChanges(message);
        }

    }//GEN-LAST:event_isEkleActionPerformed

